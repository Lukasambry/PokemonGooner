const getSavedLanguage = (): Language => {
  const saved = localStorage.getItem('pokemon-language')
  if (saved && (saved === 'en' || saved === 'fr')) {
    return saved as Language
  }
  return 'fr'
}

export const i18n = createI18n({
  locale: getSavedLanguage(),
  fallbackLocale: 'fr',
  messages,
  legacy: false
})
